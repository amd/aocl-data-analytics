#ifndef CUBIC_SPLINE_TYPES_HPP
#define CUBIC_SPLINE_TYPES_HPP

namespace da_cubic_spline {

enum spline_type_t {
    natural = 0, // Natural boundary conditions (zero second derivatives)
    clamped = 1, // Clamped boundary conditions (zero first derivatives)
    custom = 3,  // Custom first or second derivatives at endpoints
    hermite = 4  // Piecewise cubic Hermite interpolation
};

} // namespace da_cubic_spline

#endif // CUBIC_SPLINE_TYPES_HPP
